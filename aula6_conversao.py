# Conversão de tipos, coerção
# Type cenvertion, typecasting, coercion
# é o ato de converter um tipo em outro
# tipo imutáveis e primitivos:
# str, int, float, bool

print(1+1) # Soma
print("a"+"b") # Concatena
print("1", type("1")) # Imprime o valor e o tipo
print(int("1"), type(int("1"))) # Convertendo a str para int e mostrando o tipo
print(int("1") + 1) # Convertendo a str para int e somando
print(float("1") + 1) # Convertendo a str para float e somando
print(str(11) + "b") # Convertendo o int para str e concatenando